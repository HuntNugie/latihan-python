# macam macam operasi aritmatika

# pertambahan
a = 10
b = 3

hasil = a+b
print(f"{a} + {b} = {hasil}")

# pengurangan
hasil = a-b
print(f"{a} - {b} = {hasil}")

# perkalian
hasil = a*b
print(f"{a} * {b} = {hasil}")

# pembagian
hasil = a/b
print(f"{a} / {b} = {hasil}")

# modulus
hasil = a%b
print(f"{a} % {b} = {hasil}")

# exponen
hasil = a**b
print(f"{a} ** {b} = {hasil}")

# floor division
hasil = a//b
print(f"{a} // {b} = {hasil}")